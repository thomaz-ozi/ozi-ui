/**
 * components/ozi-select/lang/pt-BR.js
 */
(function (window) {
    'use strict';
    var lang = window.OZI && window.OZI.lang;
    if (!lang || !lang.register) return;
    lang.register('pt-BR', {
        select: {
            searchPlaceholder: 'Pesquisar...',
            valuePlaceholder:  'Selecione...',
            empty:             'Nenhuma opção encontrada',
            requiredMessage:   'Selecione uma opção.'
        }
    });
})(window);