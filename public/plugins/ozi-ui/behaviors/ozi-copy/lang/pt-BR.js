/**
 * behaviors/ozi-copy/lang/pt-BR.js
 */
(function (window) {
    'use strict';
    var lang = window.OZI && window.OZI.lang;
    if (!lang || !lang.register) return;
    lang.register('pt-BR', {
        copy: {
            success: 'Copiado!',
            error:   'Erro ao copiar'
        }
    });
})(window);